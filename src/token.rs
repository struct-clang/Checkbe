use crate::span::Span;

#[derive(Clone, Debug)]
pub struct Token {
    pub kind: TokenKind,
    pub span: Span,
}

#[derive(Clone, Debug)]
pub enum TokenKind {
    Identifier(String),
    IntLiteral(i64),
    FloatLiteral(f64),
    StringLiteral(String),
    Import,
    Body,
    Let,
    Func,
    Return,
    If,
    Else,
    New,
    IntType,
    FloatType,
    StringType,
    BoolType,
    True,
    False,
    LParen,
    RParen,
    LBrace,
    RBrace,
    LBracket,
    RBracket,
    Colon,
    Semicolon,
    Comma,
    Dot,
    Question,
    Plus,
    Minus,
    Arrow,
    Star,
    Slash,
    Percent,
    Assign,
    EqEq,
    NotEq,
    Greater,
    GreaterEq,
    Less,
    LessEq,
    AndAnd,
    OrOr,
    Bang,
    Eof,
}
